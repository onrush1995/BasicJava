
package beginnerjava;


public class IncreamentDecreamentDemo {
    
    public static void main(String[] args) {
        
        int x=7;
        int result;
        
        result=++x;
        System.out.println("Result = "+result);
        
        result=x++;
        System.out.println("Result = "+result);
       
        result=x;//print 9
        System.out.println("Result = "+result);
        
        result=x--;
        System.out.println("Result = "+result);
        
        result=x;
        System.out.println("Result = "+result);
        
        
        
        
        
    }
    
}
